import json
import os

script_dir = os.path.dirname(os.path.abspath(__file__))
encoder_dir = os.path.dirname(script_dir)
ml_lab_dir = os.path.dirname(encoder_dir)
json_path = os.path.join(ml_lab_dir, "TestMyWork", "ThisToxicClassificationiMsoSorry.json")

with open(json_path, "r", encoding="utf-8") as f:
    data = json.load(f)

test_texts = data

correct = 0
total = 0

for item in test_texts:
    text = item["text"]
    true_label = item["label"]

    predicted_label, confidence = predict(text, model, tokenizer)

    is_correct = (predicted_label == true_label)
    if is_correct:
        correct += 1
    total += 1

    print(f"Текст: {text}")
    print(f"Ожидалось: {true_label} Получено: {predicted_label} (уверенность: {confidence:.2f})")
    print(f"Результат: {'TRUE' if is_correct else 'FALSE'}")
    print("-" * 50)


accuracy = correct / total if total > 0 else 0
print(f"\nСТАТИСТИКА")
print(f"Правильных: {correct}/{total}")
print(f"Accuracy: {accuracy:.2%}")
