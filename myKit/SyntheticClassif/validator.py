                print(f"   ‚ö†Ô∏è –í –ø–æ–∑–∏—Ç–∏–≤–Ω–æ–º —Ç–µ–∫—Å—Ç–µ –Ω–∞–π–¥–µ–Ω—ã –Ω–µ–≥–∞—Ç–∏–≤–Ω—ã–µ —Å–ª–æ–≤–∞")
