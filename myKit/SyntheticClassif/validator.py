                "–∂–∞–ª–æ–±", "–ø—Ä–µ—Ç–µ–Ω–∑", "–Ω–µ–¥–æ–≤–æ–ª", "–ø—Ä–æ–±–ª–µ–º", "–ø–ª–æ—Ö", "—É–∂–∞—Å", 
                "–∫–æ—à–º–∞—Ä", "–æ—Ç–≤—Ä–∞—Ç", "–Ω–µ–∫–∞—á–µ—Å—Ç–≤–µ–Ω", "–æ–±–º–∞–Ω", "—Å–ª–æ–º–∞–ª", "–±—Ä–∞–∫",
